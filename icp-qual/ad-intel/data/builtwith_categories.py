# ── Convenience lookups ─────────────────────────────────────────

# Quick lookup: category_id -> category_name
CATEGORY_ID_TO_NAME: dict[str, str] = {
    cat["id"]: cat["name"] for cat in BUILTWITH_CATEGORIES
}

# Quick lookup: subcategory_id -> (parent_category_id, subcategory_name)
SUBCATEGORY_LOOKUP: dict[str, tuple[str, str]] = {}
for _cat in BUILTWITH_CATEGORIES:
    for _sub in _cat["subcategories"]:
        SUBCATEGORY_LOOKUP[_sub["id"]] = (_cat["id"], _sub["name"])

# All top-level category IDs in display order
CATEGORY_IDS: list[str] = [cat["id"] for cat in BUILTWITH_CATEGORIES]

# ICP-relevant categories (most useful for qualification pipeline)
ICP_RELEVANT_CATEGORIES: set[str] = {
    "analytics",    # Analytics and Tracking
    "ads",          # Advertising
    "widgets",      # Widgets (includes chat, marketing tools)
    "payment",      # Payment
    "mx",           # Email Hosting Providers
    "shop",         # eCommerce
    "cms",          # Content Management System
    "javascript",   # JavaScript Libraries
    "media",        # Audio / Video Media
    "framework",    # Frameworks
    "shipping",     # Shipping Providers
}


def get_category_summary() -> dict[str, int]:
    """Return dict of category_name -> subcategory_count."""
    return {
        cat["name"]: len(cat["subcategories"])
        for cat in BUILTWITH_CATEGORIES
    }


def find_subcategory(subcategory_id: str) -> dict | None:
    """Look up a subcategory by ID, returns {id, name, parent_id, parent_name}."""
    if subcategory_id in SUBCATEGORY_LOOKUP:
        parent_id, sub_name = SUBCATEGORY_LOOKUP[subcategory_id]
        return {
            "id": subcategory_id,
            "name": sub_name,
            "parent_id": parent_id,
            "parent_name": CATEGORY_ID_TO_NAME[parent_id],
        }
    return None
